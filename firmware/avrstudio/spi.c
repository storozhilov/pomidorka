//------------------------------------------------------------------------
//------------------------------------------------------------------------
void SPI_Init()
{
 DDRB |= (SS | SCK | MOSI);
 SPCR  = (1 << SPR0) | (1 << SPR1) | (1 << MSTR) | (1 << SPE) | (0 << SPIE) | (1 << CPHA) | (0 << CPOL);

}
//------------------------------------------------------------------------
void SPI_Send(unsigned char data) 
{
 SPDR = data; 
 while (!(SPSR & (1 << SPIF)));
}
